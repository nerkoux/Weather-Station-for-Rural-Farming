    die("Connection failed: " . $conn->connect_error);
} 

$sql = "SELECT id, value1, value2, value3, reading_time FROM SensorData order by reading_time desc limit 40";

$result = $conn->query($sql);

while ($data = $result->fetch_assoc()){
    $sensor_data[] = $data;
}

$readings_time = array();
foreach ($sensor_data as $data) {
    if (isset($data['reading_time'])) {
        $readings_time[] = $data['reading_time'];
    }
}


// ******* Uncomment to convert readings time array to your timezone ********
$i = 0;
foreach ($readings_time as $reading){
    $readings_time[$i] = date("Y-m-d H:i:s", strtotime("$reading + 5 hours 30 minutes"));
    $i += 1;
}

