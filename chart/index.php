    return isset($data['value1']) ? $data['value1'] : null;
}, $sensor_data)), JSON_NUMERIC_CHECK);

$value2 = json_encode(array_reverse(array_map(function($data) {
    return isset($data['value2']) ? $data['value2'] : null;
}, $sensor_data)), JSON_NUMERIC_CHECK);

$value3 = json_encode(array_reverse(array_map(function($data) {
    return isset($data['value3']) ? $data['value3'] : null;
}, $sensor_data)), JSON_NUMERIC_CHECK);

$reading_time = json_encode(array_reverse($readings_time), JSON_NUMERIC_CHECK);


